package com.bdilab.colosseum.vo;

public class PerformanceVo {
    private String systemPerformance;
    private String actualPerformance;
    private String performanceDesc;
    private String minOrMax;

    public String getSystemPerformance() {
        return systemPerformance;
    }

    public void setSystemPerformance(String systemPerformance) {
        this.systemPerformance = systemPerformance;
    }

    public String getActualPerformance() {
        return actualPerformance;
    }

    public void setActualPerformance(String actualPerformance) {
        this.actualPerformance = actualPerformance;
    }

    public String getPerformanceDesc() {
        return performanceDesc;
    }

    public void setPerformanceDesc(String performanceDesc) {
        this.performanceDesc = performanceDesc;
    }

    public String getMinOrMax() {
        return minOrMax;
    }

    public void setMinOrMax(String minOrMax) {
        this.minOrMax = minOrMax;
    }
}
